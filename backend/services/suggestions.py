def get_suggestions(query: str, locale: str = "en", limit: int = 8) -> list[str]:
    """Return job titles matching the query (case-insensitive)."""
    q = query.lower().strip()
    if not q:
        return []

    titles = JOB_TITLES.get(locale, JOB_TITLES["en"])

    prefix_matches: list[str] = []
    contains_matches: list[str] = []

    for title in titles:
        lower = title.lower()
        if lower.startswith(q):
            prefix_matches.append(title)
        elif q in lower:
            contains_matches.append(title)

    results = prefix_matches + contains_matches
    return results[:limit]
